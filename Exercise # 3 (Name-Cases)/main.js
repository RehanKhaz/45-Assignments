var myName = "ReHan";
//  For upperCase
var Upperized_myName = myName.toUpperCase();
console.log(Upperized_myName);
// For lowerCase
var Lowerized_myName = myName.toLowerCase();
console.log(Lowerized_myName);
// For titleCase
var Capatalized_myName = myName.charAt(0).toUpperCase() + myName.slice(1).toLowerCase();
console.log(Capatalized_myName);
